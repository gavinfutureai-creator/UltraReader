"""
知识提取 Prompt 模板

基于本体论的知识提取 Prompt
"""

from typing import Optional


class ExtractionPrompts:
    """
    知识提取 Prompt 模板

    基于本体论的设计原则：
    - 存在论视角：发现任何存在的事物
    - 关系论视角：用自然语言描述关系
    - 概念论视角：提炼核心概念
    """

    # System Prompt
    SYSTEM = """你是一个本体论知识工程师，负责从书籍章节中构建知识本体。

你的思维方式：

1. 存在论视角
   - "这个章节描绘了什么样的世界？"
   - "什么存在于此章节中？"
   - 不要只关注"人物"，而是关注"任何存在的事物"

2. 关系论视角
   - "这些存在之间有什么关系？"
   - "如何用一句话描述 A 和 B 的关系？"
   - 不要使用预定义的关系类型，而是用书中的自然语言

3. 概念论视角
   - "这个章节在讨论什么核心概念？"
   - "这些概念如何相互关联？"
   - 概念是理解世界的维度，而非预设的分类

重要原则：

✓ 实体类型由你根据书的实际情况发现，而非预设
✓ 关系类型由你根据书的情境命名，而非硬编码
✓ 每条信息都要标注来源章节
✓ 保持输出的可读性和连贯性
✓ 敢于创造性地发现非显而易见的实体和关系
✓ 事件描述要详细、专业，包含时间、地点、人物、起因、经过、结果等要素

排除内容（不要提取以下信息）：
- 出版社名称、出版日期、ISBN、版权声明
- 作者简介、译者简介、序言、前言（除非是正文的一部分）
- 目录页、封面信息
- 任何与故事正文无关的元数据"""

    # 详细版 User Prompt - 用于完整提取
    USER_DETAILED = """## 书名: {book_title}
## 作者: {author}
## 当前处理: 第 {chapter_index} 章 / {chapter_title}

## 历史本体（从之前章节构建的本体）:
{history_context}

## 当前章节内容:
{chapter_content}

## 你的任务

请以本体论视角分析这个章节，输出以下格式（全部使用中文）：

### 一、存在的事物

列出这个章节中出现的**任何重要存在**，使用 [[双向链接]] 格式，并标注类型。

**格式要求**：每行一个实体，格式为 `[[实体名]] (类型)`，例如：
- [[杨玉环]] (人物)
- [[长安城]] (地点)
- [[霓裳羽衣曲]] (物品)
- [[开元之治]] (概念)

**类型分类**：
- **人物**：主角、配角、历史人物、侍女、官员等
- **地点**：宫殿、城市、山川河流、室内场景、具体建筑等
- **组织**：朝廷、军队、党派、帮派、家族等
- **物品**：重要器物、信物、服饰、武器、音乐舞蹈、食物等
- **概念**：政治理念、哲学思想、情感主题、历史事件名称、制度等
- **时间**：具体年代、季节、历史时期（如"开元二十八年"）

**示例**：
[[唐玄宗李隆基]] (人物)
[[骊山温泉宫]] (地点)
[[寿王府]] (地点)
[[霓裳羽衣曲]] (物品)
[[后宫制度]] (概念)
[[开元二十八年]] (时间)

### 二、关系网络

描述这些存在之间的**具体关系**，用自然语言描述。

格式：[[实体A]] --[关系描述]--> [[实体B]]

注意：关系描述要具体，不要泛泛而谈。例如：
- [[唐玄宗]] --[召见并宠幸]--> [[杨玉环]]
- [[杨玉环]] --[被迫离开]--> [[寿王府]]
- [[李林甫]] --[陷害]--> [[太子瑛]]

### 三、重要事件

**重要**：每个事件必须包含详细的描述性陈述，不能只是简单的名称！

格式（使用表格，必须严格按照此列顺序）：
| 事件名称 | 发生时间 | 发生地点 | 主要人物 | 详细描述 |
|---------|---------|---------|---------|---------|
| ... | ... | ... | ... | ... |

**列说明**：
- **事件名称**：简洁的事件名（不超过15字），如"骊山温泉宫召见"
- **发生时间**：具体时间或时期，如"开元二十八年冬"、"天宝元年正月"
- **发生地点**：事件发生的具体地点，如"骊山温泉宫"、"长安大明宫"
- **主要人物**：参与事件的关键人物，用顿号分隔，如"唐玄宗、杨玉环、寿王李瑁"
- **详细描述**：至少2-3句话的详细描述，说明事件的起因、经过、结果

**示例**：
| 事件名称 | 发生时间 | 发生地点 | 主要人物 | 详细描述 |
|---------|---------|---------|---------|---------|
| 骊山温泉宫召见 | 开元二十八年冬 | 骊山温泉宫 | 唐玄宗、杨玉环、寿王李瑁 | 唐玄宗以泡温泉为由召见儿媳杨玉环。寿王李瑁传达父皇命令，杨玉环被迫前往。在温泉宫期间，玄宗对杨玉环一见倾心，随即开始筹谋将其接入后宫。此事件成为杨玉环命运的转折点。 |

**要求**：
- 每列必须有内容，不能留空
- 事件名称要简洁、准确（不超过15字）
- 主要人物必须是人名，不是事件描述
- 详细描述至少2-3句话

### 四、核心概念

列出这个章节讨论的**核心概念**，以及它们之间的关系。

格式：
- [[概念名称]]：对这个概念的简要解释（1-2句话）
- [[概念A]] --[关联]--> [[概念B]]（概念之间的关系）

**要求**：
- 概念要深刻，不是简单的事物名称
- 要提炼出抽象的思想、情感、制度等
- 至少提取3-5个核心概念

### 五、主题提炼

分析这一章节的**核心主题**，提炼2-3个最重要的人文主题。

格式：
- [[主题名称]]：对这个主题的阐释（2-3句话）

**示例主题**：
- [[命运的无奈]]：个人的命运往往被权力和制度所左右
- [[爱情与权力]]：在封建社会，爱情常常成为政治的牺牲品
- [[女性的困境]]：古代女性的命运掌握在男性手中

### 六、章节摘要

用一段话（100-200字）概括这一章的核心内容，包括主要情节、人物关系变化、主题呈现等。"""

    @classmethod
    def user(
        cls,
        book_title: str,
        author: str,
        chapter_index: int,
        chapter_title: str,
        history_context: str,
        chapter_content: str,
    ) -> str:
        """
        生成用户 Prompt

        Args:
            book_title: 书名
            author: 作者
            chapter_index: 章节索引 (从 1 开始)
            chapter_title: 章节标题
            history_context: 历史本体上下文
            chapter_content: 章节内容

        Returns:
            格式化的用户 Prompt
        """
        return f"""## 书名: {book_title}
## 作者: {author}
## 当前处理: 第 {chapter_index} 章 / {chapter_title}

## 历史本体（从之前章节构建的本体）:
{history_context}

## 当前章节内容:
{chapter_content}

## 你的任务

请以本体论视角分析这个章节，输出以下格式（全部使用中文）：

### 一、存在的事物

列出这个章节中出现的**任何重要存在**，使用 [[双向链接]] 格式，例如：[[杨玉环]]。

包括但不限于：
- 人物
- 地点/空间
- 组织/团体
- 物品/道具
- 概念/思想
- 事件/行动
- 时间/时期

### 二、关系网络

描述这些存在之间的**具体关系**，用自然语言描述：

格式：[[实体A]] --[关系描述]--> [[实体B]]

注意：关系描述要具体，不要泛泛而谈

### 三、重要事件

列出这个章节中的**重要事件**：
- 事件名称
- 参与者
- 事件意义

### 四、核心概念

列出这个章节讨论的**核心概念**，以及它们之间的关系。

### 五、章节摘要

用简洁的语言概括这一章的内容。"""

    @classmethod
    def system_simple(cls) -> str:
        """简化版 System Prompt"""
